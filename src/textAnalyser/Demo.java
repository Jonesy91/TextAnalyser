package textAnalyser;

public class Demo 
{
	// String variable for the text to be analysed in the demo
	private String demoInput;

	// Class constructor which sets the value of the demoInput variable
	public Demo()
	{
		demoInput = "This is a demonstration of the text analyser.\nThis text is used to demonstrate the application!\n ";
	}
	
	// Method to get the value of the demoInput variable
	public String getDemoInput()
	{
		return demoInput;
	}
	
	// toString method which returns the value of the demoInput variable
	public String toString()
	{
		return demoInput;
	}
}
